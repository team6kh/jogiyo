package common.registration.dto;

public class RegistrationDTO
{
    private String reg_type;
    private String reg_id;
    private String reg_name;
    private String reg_pw;
    private String reg_email;
    private String reg_phonenumber;
    private String reg_gender;

    public String getReg_type()
    {
        return reg_type;
    }

    public void setReg_type(String reg_type)
    {
        this.reg_type = reg_type;
    }

    public String getReg_id()
    {
        return reg_id;
    }

    public void setReg_id(String reg_id)
    {
        this.reg_id = reg_id;
    }

    public String getReg_name()
    {
        return reg_name;
    }

    public void setReg_name(String reg_name)
    {
        this.reg_name = reg_name;
    }

    public String getReg_pw()
    {
        return reg_pw;
    }

    public void setReg_pw(String reg_pw)
    {
        this.reg_pw = reg_pw;
    }

    public String getReg_email()
    {
        return reg_email;
    }

    public void setReg_email(String reg_email)
    {
        this.reg_email = reg_email;
    }

    public String getReg_phonenumber()
    {
        return reg_phonenumber;
    }

    public void setReg_phonenumber(String reg_phonenumber)
    {
        this.reg_phonenumber = reg_phonenumber;
    }

    public String getReg_gender()
    {
        return reg_gender;
    }

    public void setReg_gender(String reg_gender)
    {
        this.reg_gender = reg_gender;
    }
}